				<h2>OBEDIENCIA PARA PERROS CON MIEDO AL AGUA</h2>
				<article>
					<p>
						Los perros que tienen miedo a entrar a una pileta, es consecuencia de una 
						experiencia traumática. Los han arrojado al agua para que aprendieran a nadar 
						o bien, se han resbalado y caído al agua.
					</p>
					<p>
						También hay casos que sin haber existido ninguna experiencia de este tipo, tienen 
						pánico a entrar a una pileta. En cualquiera de las dos situaciones, el 
						adiestramiento se planifica y desarrolla teniendo en cuenta el temor del perro 
						al agua.
					</p>
					<p>
						El primer paso es acercarlo a la orilla de la pileta y con palabras 
						cari&ntilde;osas y caricias, comenzar a motivarlo para que se zambulla. Es 
						fundamental la participación de su propietario, debido a que es el mayor 
						referente de confianza y seguridad para el perro.
					</p>
					<p>
						Cuando se logra que entre a la pileta, hay que mantenerlo abrazado unos 
						minutos y luego ayudarlo a salir. Esta actitud protectora actúa de forma muy 
						positiva sobre el perro, se tranquiliza y pone menos resistencia a la 
						enseñanza.
					</p>
					<p>
						Cuando pierde el miedo de entrar a la pileta, se comienza a nadar junto a él. 
						En esta etapa del adiestramiento, el perro ya no tiene miedo y empieza a 
						disfrutar del agua y de nadar junto a su propietario. En este última etapa 
						del adiestramiento, el perro logra zambullirse y nadar solo.
					</p>
					<p>
						El cariño, la paciencia y la protección de parte del entrenador y de su 
						propietario le posibilitará al perro resolver el miedo al agua.
					</p>
				</article>
