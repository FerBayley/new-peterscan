				<h2>OBEDIENCIA PARA PERROS EFUSIVOS EN EL AGUA</h2>
				<article>
					<p>
						El adiestramiento de un perro en la piscina, consiste en ense&ntildE;arle 
						ejercicios de obediencia que permitan a través de una orden, lograr que el 
						perro entre y salga de la piscina o se quede en los bordes de la misma.
					</p>
					<p>
						Esta enseñanza nos permitirá nadar, jugar o simplemente disfrutar del agua, 
						evitando así, que el perro salte sobre nosotros golpeándonos o lastimándonos 
						con las uñas o con los dientes.
					</p>
					<p>
						Los perros que presentan esta conducta no tienen la intención de hacernos 
						daño cuando nos ven en piscina, lo que sucede es que tienen la necesidad 
						imperiosa de sacarnos del agua por que creen que estamos en peligro.
					</p>
					<p>El motivo de esta actitud en algunos perros, es debido a que tienen en su bagaje genético la impronta de rescate.</p>
					<p>
						Mante esta cualidad, lo conveniente es no censurarlo, sino canalizar a través 
						de un adiestramiento adecuado la bondad genética y enseñarle a que distinga 
						cuando una persona está en peligro de ahogarse o está nadando o jugando en el 
						agua.
					</p>
					<p>
						El aprendizaje que realiza el perro en la piscina y su entorno permite a sus 
						propietarios disfrutar de su compañía y del agua. Por último, aprende a 
						entrar, nadar y salir de la piscina.
					</p>
				</article>
