				<h2>OBEDIENCIA PARA PERROS CON MIEDO A LOS RUIDOS</h2>
				<article>
					<p>
						Las reacciones por miedos más frecuentes en los perros son debido a los ruidos 
						provocados por truenos, caños de escape de autos y colectivos, explosiones 
						pirotécnicas, sirenas, gritos, alarmas etc.
					</p>
					<p>
						Ante una situación de pánico generada por un ruido, los perros tienden a escaparse 
						del lugar, muchos son atropellados por un auto o se pierden por este motivo , se 
						paralizan, aúllan, tiemblan o se ponen en dos patas sobre un árbol o pared como 
						buscando una puerta virtual que les permita alejarse del lugar.
					</p>
					<p>
						¿Cómo se resuelve? Recibiendo INFORMACION sobre las características y 
						necesidades del perro temeroso de los ruidos.
					</p>
					<p>
						IDENTIFICANDO los ruidos que lo perturban.Estableciendo una ESTRATEGIA de 
						TRABAJO de acuerdo a la raza, edad y medio ambiente familiar.
					</p>
					<p>
						PLANIFICANDO LOS EJERCICIOS de ADIESTRAMIENTO en Socialización y Obediencia, que 
						permitan resolver esta conducta problemática. TENIENDO MUCHA PACIENCIA no es 
