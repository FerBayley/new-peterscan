					</p>

					<p>
						<strong>Pasear sin dificultad</strong>
						Hay paseos que no podemos realizar con nuestro cachorro debido a que salta sobre 
						la gente, tira con mucha fuerza de la correa, o cruza de un lado a otro por delante 
						nuestro mientras caminamos. A trav&eacute;s del adiestramiento aprende a 
						acompa&ntilde;arnos sin dificultad en los paseos.
					</p>

